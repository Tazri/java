class SomeMath{
    public static void main(String []args){
        System.out.println("2 + 3 = " + (2+3)); // 2 + 3 = 5
        System.out.println("2 - 3 = " + (2-3)); // 2 - 3 = -1
        System.out.println("2 * 3 = " + (2*3)); // 2 * 3 =  6
        System.out.println("4 / 2 = " + (4/2)); // 4 / 2 = 2
    }
}